"""
Перечисления и константы для калькулятора госпошлины.
"""
from enum import Enum, auto


class CourtType(Enum):
    """Тип суда."""
    GENERAL = "general"        # Суд общей юрисдикции / мировой судья / ВС по ГПК/КАС
    ARBITRATION = "arbitration"  # Арбитражный суд / ВС по АПК


class ClaimType(Enum):
    """Тип иска / заявления."""
    # ─── Имущественные (подлежащие оценке) ───────────────────────────────────
    PROPERTY = "property"                    # Имущественный (с ценой иска)

    # ─── Неимущественные / имущественные без оценки ──────────────────────────
    NON_PROPERTY = "non_property"            # Неимущественный или не подлежащий оценке

    # ─── Договорные споры (без возврата исполненного / без последствий) ───────
    CONTRACT_DISPUTE = "contract_dispute"    # Заключение/изм/расторжение без возврата;
                                             # признание сделки недействительной без последствий

    # ─── Семейные ─────────────────────────────────────────────────────────────
    DIVORCE = "divorce"                      # Расторжение брака

    # ─── Алименты ─────────────────────────────────────────────────────────────
    ALIMONY = "alimony"                      # Взыскание алиментов

    # ─── Административные ────────────────────────────────────────────────────
    ADMIN_NORMATIVE = "admin_normative"      # Оспаривание норм. правовых актов
    ADMIN_NON_NORMATIVE = "admin_non_normative"  # Оспаривание ненорм. актов / действий (бездействия)

    # ─── Особые производства ──────────────────────────────────────────────────
    SPECIAL_PROCEEDING = "special_proceeding"   # Дела особого производства (общ. юрисдикция)
    FACT_ESTABLISHMENT = "fact_establishment"   # Установление фактов (арбитраж)

    # ─── Судебный приказ ──────────────────────────────────────────────────────
    COURT_ORDER = "court_order"              # Заявление о вынесении судебного приказа

    # ─── Обеспечительные меры ─────────────────────────────────────────────────
    INTERIM_MEASURES = "interim_measures"    # Обеспечение иска / замена / отмена

    # ─── Апелляция / кассация / надзор ────────────────────────────────────────
    APPEAL = "appeal"                        # Апелляционная жалоба / частная жалоба / касс. на приказ
    CASSATION = "cassation"                  # Кассационная жалоба
    SUPREME_CASSATION = "supreme_cassation"  # Кассационная/надзорная жалоба в ВС РФ

    # ─── Банкротство (арбитраж) ───────────────────────────────────────────────
    BANKRUPTCY = "bankruptcy"                # Признание должника банкротом
    BANKRUPTCY_DISPUTE = "bankruptcy_dispute"  # Обособленный спор в деле о банкротстве

    # ─── Третейские суды / иностранные решения ────────────────────────────────
    ENFORCEMENT_FOREIGN = "enforcement_foreign"  # Выдача исп. листа на реш. третейского/иностр. суда
    CANCEL_ARBITRATION = "cancel_arbitration"    # Отмена решения третейского суда

    # ─── Пересмотр ────────────────────────────────────────────────────────────
    REVIEW_NEW_CIRCUMSTANCES = "review_new_circumstances"  # Пересмотр по новым/вновь открывш. обст.

    # ─── Исполнительное производство ──────────────────────────────────────────
    DUPLICATE_WRIT = "duplicate_writ"        # Дубликат исп. листа / пересмотр заочного решения
    EXECUTION_ISSUES = "execution_issues"    # Отсрочка/рассрочка/разъяснение исп. листа
    RESTORE_DEADLINE = "restore_deadline"    # Общей юрисдикции: восстановление срока / отсрочка / поворот

    # ─── Правопреемство ───────────────────────────────────────────────────────
    SUCCESSION = "succession"                # Заявление о правопреемстве

    # ─── Компенсации ──────────────────────────────────────────────────────────
    COMPENSATION_DELAY = "compensation_delay"          # Компенсация за нарушение разумного срока
    COMPENSATION_DETENTION = "compensation_detention"  # Компенсация за условия содержания (только общ.)

    # ─── ИС / патенты (арбитраж) ──────────────────────────────────────────────
    IP_NORMATIVE = "ip_normative"            # Оспаривание НПА фед. органов в сфере ИС
    IP_CLARIFICATION = "ip_clarification"    # Оспаривание актов с разъяснениями (норм. свойства) в сфере ИС


class ApplicantType(Enum):
    """Тип заявителя."""
    INDIVIDUAL = "individual"    # Физическое лицо
    ORGANIZATION = "organization"  # Организация / ИП (для арбитража и повышенных ставок)


class ExemptionType(Enum):
    """Основания для льгот (освобождение от пошлины)."""
    NONE = "none"                       # Нет льгот
    ALIMONY_PLAINTIFF = "alimony"       # Истец по алиментам
    LABOR_DISPUTE = "labor"             # Работник — трудовой спор
    CONSUMER_PROTECTION = "consumer"    # Защита прав потребителей
    HEALTH_DAMAGE = "health"            # Вред здоровью / смерть кормильца
    CRIME_VICTIM = "crime"              # Уголовное дело (реабилитация)
    DISABILITY_1_2 = "disability"       # Инвалид I или II группы
    PENSION_SOCIAL = "pension_social"   # Пенсионеры/получатели пособий по соц. делам
    PROSECUTOR = "prosecutor"           # Прокурор / госорган в защиту публичных интересов
    BANKRUPTCY_DEBTOR = "bankruptcy_debtor"  # Должник, сам подающий на банкротство
    VETERAN = "veteran"                 # Ветеран (по отдельным делам)
